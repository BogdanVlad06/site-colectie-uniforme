// Function to initialize the language on page load
function initializeLanguage() {
    // Check if a language is stored in localStorage
    const storedLanguage = localStorage.getItem('language');
    
    // If a language is stored, use it. Otherwise, default to 'ro' (or any default language you prefer)
    const languageToUse = storedLanguage ? storedLanguage : 'ro';
    
    // Set the language selector to the stored value (or default)
    document.getElementById("language-selector").value = languageToUse;

    // Call the changeLanguage function to update the page content
    changeLanguage();
}

// Call the initializeLanguage function when the page loads
document.addEventListener('DOMContentLoaded', initializeLanguage);

// Add event listener to handle language changes from the selector
document.getElementById("language-selector").addEventListener('change', changeLanguage);